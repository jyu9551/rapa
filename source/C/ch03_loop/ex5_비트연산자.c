#include <stdio.h>
// 비트연산자 : &, |, ^, >>, <<

int main(){
    int n1 = 10; // ... 1010
    int n2 = 6;  // ... 0110

    printf("%d\n", n1 & n2); // ... 0010 (2)
    printf("%d\n", n1 | n2); // ... 1110 (14)
    printf("%d\n", n1 ^ n2); // ... 1100 (12)
    printf("%d\n", n1 >> 1); // ... 0101 (5)  = /2 연산
    printf("%d\n", n2 << 1); // ... 1100 (12) = x2 연산
}